
import { IPoint } from "common/geometry/point.js";

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

const pointToString = (point:IPoint):string => `${point.x} ${point.y}`;

const pointsToString = (points:Array<IPoint>):string => points.map(pointToString).join(" ");

//////////////////////////////////////////////////////////////////////////////////////////////////////////////

export
	{
	pointToString,
	pointsToString
	};
